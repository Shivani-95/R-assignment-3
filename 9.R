#Write R code that concatenates the strings "Data", "Science", and "R" into a single string with hyphens (-) as 
#separators.
#10. 
# Define the strings
str1 <- "Data"
str2 <- "Science"
str3 <- "R"

# Concatenate with hyphens
concatenated_str <- paste(str1, str2, str3, sep = "-")

# Print the result
print(concatenated_str)


